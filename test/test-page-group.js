test("page-group", function(){
  var grp = new PageGroup(2);
  var box1 = new Box(new BoxSize(100,100), null, "div");
  var box2 = new Box(new BoxSize(100,100), null, "div");
  box1.lazy = true;
  box1.pageNo = 0;
  box2.lazy = true;
  box2.pageNo = 1;
  equal(grp.getSize(), 2);
  equal(grp.isEmpty(), true);
  grp.add(box1);
  equal(grp.isEmpty(), false);
  equal(grp.isComplete(), false);
  equal(grp.getSize(), 2);
  equal(grp.getLength(), 1);
  deepEqual(grp.getFirst(), box1);
  deepEqual(grp.getLast(), box1);
  grp.add(box2);
  equal(grp.isEmpty(), false);
  equal(grp.isComplete(), true);
  equal(grp.getLength(), 2);
  deepEqual(grp.getFirst(), box1);
  deepEqual(grp.getLast(), box2);
  deepEqual(grp.get(0), box1);
  deepEqual(grp.get(1), box2);
  grp.commit();
  equal(grp.lazy, true);
  box1.lazy = false;
  grp.commit();
  equal(grp.lazy, false);
  equal(grp.pageNo, 0);
});
